// Que es el dom y windows object.

// DOM Document Object Model:

// Es una representación en forma de árbol del contenido de un documento HTML.
// Nos permite acceder y manipular los elementos y estructura de la página web desde JavaScript, 
// facilitando cambios dinámicos e interacciones

// Window Object:

// Es el objeto global en el navegador que representa la ventana del navegador. 
// Además del DOM tiene acceso a otros recursos como el historial y la localización. 
// Permite también interactuar con hardware a través de API específicas.

